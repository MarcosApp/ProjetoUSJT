private void CarregarChefe(){
      try {
            Conexao conexao;
            conexao = new Conexao();
            String sql = "SELECT * FROM chefedochefe";
            PreparedStatement pst = conexao.getConexao().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String chefes = rs.getString("Nome");
                combChefeM.addItem(chefes);
            }
            
            
            
        }catch(SQLException e){
            
        }
    }
    public int enviarID(){
         try {
            Conexao conexao;
            conexao = new Conexao();
            int id = 0;
            String sql = "SELECT idChefedoChefe from chefedochefe where Nome = ?";
            PreparedStatement pst = conexao.getConexao().prepareStatement(sql);
            pst.setString(1,combChefeM.getSelectedItem().toString() );
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("idChefedoChefe");
                return id;
            }
            else{
                JOptionPane.showMessageDialog(null, "Nenhum Chefe Encontrado");
            }
        }catch(SQLException e){
            
        }
        return 0;
    }
