    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (txtNomeChefe.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Por favor, preenchar todas os campos corretamente", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                Funcionalidades func = new Funcionalidades();
                UserLogin user = new UserLogin();
                ChefeImediato chefe = new ChefeImediato();
                chefe.setNome(this.txtNomeChefe.getText());
                chefe.setIdChefeImediato(enviarID());
                user.setLogin(logintxt.getText());
                user.setSenha(Senhatxt.getText());
                user.setPerfil("Chefe Imediato");
                chefe.setIdLogin(func.cadastrarLogin(user));
                if (func.cadastrarChefe(chefe)) {
                    JOptionPane.showMessageDialog(this, "Novo Chefe Cadastrado", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                }
                else{
                    JOptionPane.showMessageDialog(this, "Ocorreu um Erro", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Chefe.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

