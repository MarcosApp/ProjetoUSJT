    private void btnEnviarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarPedidoActionPerformed
        if (txtNome.getText().equals("") || txtCpf.getText().equals("") || txtRg.getText().equals("") || txtCargo.getText().equals("") || txtDataNasc.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Por favor, preenchar todas os campos corretamente", "Informação", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                Funcionalidades func = new Funcionalidades();
                UserEmpregado empregado = new UserEmpregado();
                empregado.setNome(this.txtNome.getText());
                empregado.setCpf(this.txtCpf.getText());
                empregado.setRg(this.txtRg.getText());
                String dia = txtDataNasc.getText().substring(0, 2);
                String mes = txtDataNasc.getText().substring(3, 5);
                String ano = txtDataNasc.getText().substring(6);
                String dataMysql = ano + "-" + mes + "-" + dia;
                empregado.setDataNascimento(dataMysql);
                empregado.setCargo(this.txtCargo.getText());
                UserLogin login = new UserLogin();
                login.setLogin(logintxt.getText());
                login.setSenha(Senhatxt.getText());
                login.setPerfil("Empregado");
                empregado.setChefeImediato(enviarID());
                empregado.setLoginSistema(func.cadastrarLogin(login));
                if (func.cadastrarEmpregado(empregado)) {
                    JOptionPane.showMessageDialog(this, "Novo Empregado Cadastrado", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);

                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um Erro", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FormularioEmpregado.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnEnviarPedidoActionPerformed
