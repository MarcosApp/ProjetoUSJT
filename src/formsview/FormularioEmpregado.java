    private void CarregarChefe(){
      try {
            Conexao conexao;
            conexao = new Conexao();
            String sql = "SELECT * FROM chefeimediato";
            PreparedStatement pst = conexao.getConexao().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String chefes = rs.getString("Nome");
                combchefe.addItem(chefes);
            }
            
            
        }catch(SQLException e){
            
        }
    }
    public int enviarID(){
         try {
            Conexao conexao;
            conexao = new Conexao();
            int id = 0;
            String sql = "SELECT idChefeImediato from chefeimediato where Nome = ?";
            PreparedStatement pst = conexao.getConexao().prepareStatement(sql);
            pst.setString(1,combchefe.getSelectedItem().toString() );
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("idChefeImediato");
                return id;
            }else{
                JOptionPane.showMessageDialog(null,"Não foi encontrado nenhum chefe");
            }
        }catch(SQLException e){
            
        }
        return 0;
    }
