    private void btnConsultReembMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConsultReembMouseClicked
        Reembolso rb = new Reembolso();
        rb.setMesRef(this.jComboBoxMes1.getSelectedItem().toString());
        Funcionalidades func = new Funcionalidades();
        this.lblValorPrestacao.setText(func.ValorPrestacao(rb.getMesRef()));
        this.lblValorReembolso.setText(func.valorRembolso(rb.getMesRef()));
        
        double numero = Integer.parseInt(lblValorPrestacao.getText());
        double numero2 = Integer.parseInt(lblValorReembolso.getText());
        double resultado = numero + numero2;
        NumberFormat nf = NumberFormat.getNumberInstance();
        nf.setGroupingUsed(true);
        
        String total = nf.format(resultado);
        String texto = String.valueOf(total);
        this.lblValorTotal.setText("R$: ".concat(texto));
        
        func.ConsultaPedidoReembo(rb.getMesRef());
        func.ConsultaPedidoPrest(rb.getMesRef());
        // TODO add your handling code here:
    }//GEN-LAST:event_btnConsultReembMouseClicked

