    private void btnAprovarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAprovarMouseClicked
        Funcionalidades func = new Funcionalidades();
        int idChefe = Integer.parseInt(id.getText());
        if (func.AprovarPedido(Integer.parseInt(combID.getSelectedItem().toString()))) {
            JOptionPane.showMessageDialog(this, "Pedido Aprovado", "Informação", JOptionPane.INFORMATION_MESSAGE);
            StatusPedido sp = new StatusPedido();
            sp.setResultado(this.combStatus.getSelectedItem().toString());
            func.ConsultaGeralPrestChef(sp.getResultado(), idChefe);
            func.ConsultaGeralReembolsoChef(sp.getResultado(),idChefe);
            combID.removeAllItems();
            IdsComboxReemb(sp.getResultado(),idChefe);
            IdsComboxPrest(sp.getResultado(),idChefe);

        } else {
            JOptionPane.showMessageDialog(this, "ID Incorreto", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAprovarMouseClicked

