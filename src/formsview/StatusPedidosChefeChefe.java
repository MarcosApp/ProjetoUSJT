    private void btnReprovarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnReprovarMouseClicked
        Funcionalidades func = new Funcionalidades();
        if (func.ReprovarPedido(Integer.parseInt(idComb.getSelectedItem().toString()))) {
            JOptionPane.showMessageDialog(this, "Pedido Reprovado", "Informação", JOptionPane.INFORMATION_MESSAGE);
            StatusPedido sp = new StatusPedido();
            sp.setResultado(this.comStatusChefe.getSelectedItem().toString());
            func.ConsultaGeralPrestChefs(sp.getResultado());
            func.ConsultaGeralReembolsoChefs(sp.getResultado());
            idComb.removeAllItems();
            IdsComboxPrests(sp.getResultado());
            IdsComboxReembs(sp.getResultado());
        } else {
            JOptionPane.showMessageDialog(this, "ID Incorreto", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnReprovarMouseClicked

